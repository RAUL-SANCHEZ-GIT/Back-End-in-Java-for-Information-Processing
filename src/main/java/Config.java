public class Config {

    private String pathReader;
    private String pathWriter;
    private char delimiter;

    public String getPathReader() {
        return pathReader;
    }

    public void setPathReader(String pathReader) {
        this.pathReader = pathReader;
    }

    public String getPathWriter() {
        return pathWriter;
    }

    public void setPathWriter(String pathWriter) {
        this.pathWriter = pathWriter;
    }

    public char getDelimiter() {
        return delimiter;
    }

    public void setDelimiter(char delimiter) {
        this.delimiter = delimiter;
    }
}
